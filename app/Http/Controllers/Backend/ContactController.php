    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Request $request)
    {
        $id = $request->id;
        $contact = Contact::find($id);
        $contact->delete();
        return response()->json(['success'=>'Đổi mật khẩu thành công !']);
